function replace(array, origin, target) {
    // const replaced = array.map(item => item == origin ? target : item);
    
    const replaced = Array.from(array);
    for (let i = 0; i < replaced.length; i++) {
        if (replaced[i] === origin) {
            replaced[i] = target
        }
    }
    return replaced;
}

